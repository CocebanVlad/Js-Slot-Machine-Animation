function shuffleArray(array) {
  for (var i = array.length - 1; i > 0; i--) {
    var j = Math.floor(Math.random() * (i + 1));
    var temp = array[i];
    array[i] = array[j];
    array[j] = temp;
  }

  return array;
}

function execFn(fn, args, ctx) {
  if (typeof fn !== "function") return;
  fn.apply(ctx, args);
}
